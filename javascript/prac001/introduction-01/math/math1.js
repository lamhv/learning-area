// Integers are floating-point numbers without a fraction
// Floating point numbers (floats) have decimal points and decimal places

let finalRs;
let evenOrOdd;

const number1 = 4;
const number2 = 8;
const number3 = 12;
const number4 = 8;

const addRs = number1 + number2;
const subtractRs = number3 - number4;

finalRs = addRs * subtractRs;
evenOrOdd = finalRs % 2;